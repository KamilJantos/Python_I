# operatory arytmetyczne
suma = 1 + 2 * 3 / 4.0
print(suma)
# modulo czyli reszta z dzielenia
reszta = 12 % 5
print(reszta)
kwadrat = 5 ** 2
print(kwadrat)
szescian = 5 ** 3
print(szescian)
# operacje na napisach
full_name = "Jan" + " " + "Nowak"
print(full_name)
# ale to ?
spam = "SPAM " * 10
print(spam)
# listy
oceny = [1, 2, 3, 4, 5] * 10
print(oceny)
# operatory porównania
liczba1 = 1
liczba2 = 2
print(liczba1 > liczba2)
print(liczba1 <= liczba2)
print(liczba1 == liczba2)
print(liczba1 != liczba2)
# powyższe porównania zwrócą wartości logiczne czyli True lub False
# na wartościach logicznych możemy również wykonywać operacje
prawda = True
falsz = False
print(prawda and falsz)
print(prawda or falsz)
print(not prawda)
print(not not prawda)
print(bool(prawda or falsz))
